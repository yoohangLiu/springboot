package com.xs.boot.entity;

public class StudentInfo {
    int student_id;
    String zkao_num;
    String stu_name;
    String sz_num;
    String qx_num;
    int bm_state;

    public int getStudent_id() {
        return student_id;
    }

    public void setStudent_id(int student_id) {
        this.student_id = student_id;
    }

    public String getZkao_num() {
        return zkao_num;
    }

    public void setZkao_num(String zkao_num) {
        this.zkao_num = zkao_num;
    }

    public String getStu_name() {
        return stu_name;
    }

    public void setStu_name(String stu_name) {
        this.stu_name = stu_name;
    }

    public String getSz_num() {
        return sz_num;
    }

    public void setSz_num(String sz_num) {
        this.sz_num = sz_num;
    }

    public String getQx_num() {
        return qx_num;
    }

    public void setQx_num(String qx_num) {
        this.qx_num = qx_num;
    }

    public int getBm_state() {
        return bm_state;
    }

    public void setBm_state(int bm_state) {
        this.bm_state = bm_state;
    }
}
